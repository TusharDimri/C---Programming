    /*While loop in C++*/
    // Syntax:
    // while(condition)
    // {
    //     C++ statements;
    // }

    //  Printing 1 to 40 using while loop
    int b=1;
    while(b<=40){
        cout<<b<<" ";
        b++;
    }

