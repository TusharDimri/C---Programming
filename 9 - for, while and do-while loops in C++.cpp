    /* do While loop in C++*/
        // Syntax:
        // do
        // {
        //     C++ statements;
        // }while(condition);
        
    //  Printing 1 to 40 using while loop
    int c=1;
    do{
        cout<<c<<" ";
        c++;
    }while( c<= 40);

